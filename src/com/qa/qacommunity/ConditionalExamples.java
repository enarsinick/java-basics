public class ConditionalExamples {

	public static void main(String[] args) {

		
	
	}
	
	public static void canNickJava() {
		
		boolean canNickJava = false;
		
		if (canNickJava) {
			System.out.println("Nick can Java!");
		} else {
			System.out.println("Nick can not java the absolute mug");
		}
		
	}
	
	public static void numbersJava() {
		
		int number = 24;
		
		if (number <= 20) {
			System.out.println("This number is lower or equal to 20");
		} else if (number < 40) {
			System.out.println("Number is greater than 20 but less than 40");
		} else {
			System.out.println("This number is greater than 20");
		}
		
	}

}
